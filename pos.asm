;---------------Code Segment----------------
.CODE

MAIN PROC
	MOV AX,@DATA  ; Define data segment
	MOV DS,AX

;--------------codes
AGAIN:
	MOV AH, 09H
	LEA DX, M1
	INT 21H

	MOV AH, 09H
	LEA DX, M2
	INT 21H

	MOV AH, 09H
	LEA DX, M3
	INT 21H

	MOV AH, 09H
	LEA DX, M4
	INT 21H

	MOV AH, 09H
	LEA DX, M5
	INT 21H

	MOV AH, 09H
	LEA DX, M6
	INT 21H

	MOV AH, 09H
	LEA DX, M7
	INT 21H

	MOV AH, 09H
	LEA DX, M8
	INT 21H

	MOV AH, 09H
	LEA DX, M9
	INT 21H

	MOV AH, 09H
	LEA DX, M10
	INT 21H

	MOV AH, 01H
	INT 21H
	MOV BH, AL
	SUB BH, 48
	
	CMP BH, 1		; GO TO CHECKOUT IF CHOICE = 1
	JNE CHECKOUT

	CMP BH, 2		; GO TO INVENTORY IF CHOICE = 3
	JNE INVENTORY

	CMP BH, 3		; GO TO CUSTOMER IF CHOICE = 3
	JNE CUSTOMER

	CMP BH, 4		; GO TO SALES IF CHOICE = 4
	JNE SALES

	CMP BH, 5		; GO TO RETURN IF CHOICE = 5
	JNE RETURN

	CMP BH, 6		; GO TO STAFF IF CHOICE = 6
	JNE STAFF

	CMP BH, 7		; EXIT IF CHOICE = 7
	JNE EXIT

	JNE INVALID


INVALID:			; INVaLID CHOICE
	MOV AH, 09H
	LEA DX, MSG2
	INT 21H
	JMP AGAIN

EXIT:				; EXIT PROGRAM
	MOV AH, 09H
	LEA DX, MSG1
	INT 21H
	MOV AX, 4C00H
	INT 21H

	MAIN ENDP
END MAIN