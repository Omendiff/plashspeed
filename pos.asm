.MODEL SMALL
.STACK 64

;---------------Data Segment----------------
.DATA	;data definition 
;---main menu
	M1 DB 10, 13, "-----PLASHSPEED POS----- $"
	M2 DB 10, 13, "1. CHECKOUT $"
	M3 DB 10, 13, "2. INVENTORY MANAGEMENT $"
	M4 DB 10, 13, "3. CUSTOMER MANAGEMENT $"
	M5 DB 10, 13, "4. SALES REPORTING $"
	M6 DB 10, 13, "5. RETURN AND REFUND $"
	M7 DB 10, 13, "6. STAFF MANAGEMNT $"
	M8 DB 10, 13, "7. EXIT $"
	M9 DB 10, 13, "  $"
	M10 DB 10, 13, "CHOICE: $"
	


;---staff menu 1
	STF_M1 DB 10, 13, "--STAFF MANAGEMENT-- $"
	STF_M2 DB 10, 13, "1. STAFF REPORT $"
	STF_M3 DB 10, 13, "2. STAFF INFO $"
	STF_M4 DB 10, 13, "3. EXIT $"
	STF_M5 DB 10, 13, "  $"
	STF_M6 DB 10, 13, "CHOICE $" 
;---staff menu 2
	STF_M7 DB 10, 13, "----STAFF INFO---- $"
	STF_M8 DB 10, 13, "1. CURRENT STAFF $"
	STF_M9 DB 10, 13, "2. NEW STAFF $"
	STF_M10 DB 10, 13, "3. EXIT $"
	STF_M11 DB 10, 13, "  $"
	STF_M12 DB 10, 13, "CHOICE $"
;---staff menu 3
	STF_M13 DB 10, 13, "--CURRENT STAFF-- $"
	STF_M14 DB 10, 13, "1. CHANGE PASSWORD $"
	STF_M15 DB 10, 13, "2. REMOVE STAFF $"
	STF_M16 DB 10, 13, "3. EXIT $"
	STF_M17 DB 10, 13, "  $"
	STF_M18 DB 10, 13, "CHOICE $"

;---SYSTEM MESSAGE
	MSG1 DB 10, 13, "EXITING PLASHSPEED..... $"
	MSG2 DB 10, 13, "INVALID CHOICE, PLEASE TRY AGAIN!!! $"

;---------------Code Segment----------------
.CODE

MAIN PROC
	MOV AX,@DATA  ; Define data segment
	MOV DS,AX

;--------------codes
AGAIN:
	MOV AH, 09H
	LEA DX, M1
	INT 21H

	MOV AH, 09H
	LEA DX, M2
	INT 21H

	MOV AH, 09H
	LEA DX, M3
	INT 21H

	MOV AH, 09H
	LEA DX, M4
	INT 21H

	MOV AH, 09H
	LEA DX, M5
	INT 21H

	MOV AH, 09H
	LEA DX, M6
	INT 21H

	MOV AH, 09H
	LEA DX, M7
	INT 21H

	MOV AH, 09H
	LEA DX, M8
	INT 21H

	MOV AH, 09H
	LEA DX, M9
	INT 21H

	MOV AH, 09H
	LEA DX, M10
	INT 21H

	MOV AH, 01H		; USER ENTER CHOICE
	INT 21H
	MOV BH, AL
	SUB BH, 48
	
	CMP BH, 1		; GO TO CHECKOUT IF CHOICE = 1
	JE CHECKOUT

	CMP BH, 2		; GO TO INVENTORY IF CHOICE = 3
	JE INVENTORY

	CMP BH, 3		; GO TO CUSTOMER IF CHOICE = 3
	JE CUSTOMER

	CMP BH, 4		; GO TO SALES IF CHOICE = 4
	JE SALES

	CMP BH, 5		; GO TO RETURN IF CHOICE = 5
	JE RETURN

	CMP BH, 6		; GO TO STAFF IF CHOICE = 6
	JE STAFF

	CMP BH, 7		; EXIT IF CHOICE = 7
	JE EXIT

	JMP INVALID

;--------------STAFF MANAGEMENT
STAFF:
	MOV AH, 09H
	LEA DX, STF_M1
	INT 21H

	MOV AH, 09H
	LEA DX, STF_M2
	INT 21H

	MOV AH, 09H
	LEA DX, STF_M3
	INT 21H

	MOV AH, 09H
	LEA DX, STF_M4
	INT 21H

	MOV AH, 09H
	LEA DX, STF_M5
	INT 21H

	MOV AH, 09H
	LEA DX, STF_M6
	INT 21H

	MOV AH, 01H			; USER ENTER CHOICE
	INT 21H
	MOV BL, AL
	SUB BL, 48

	CMP BL, 1			; REPORT
	JE STF_REPORT

	CMP BL, 2			; INFO
	JE STF_INFO

	CMP BL, 3			; EXIT
	JE AGAIN

	JMP STF_INVALID1	; INVALID

STF_INFO:
	MOV AH, 09H
	LEA DX, STF_M7
	INT 21H

	MOV AH, 09H
	LEA DX, STF_M8
	INT 21H

	MOV AH, 09H
	LEA DX, STF_M9
	INT 21H

	MOV AH, 09H
	LEA DX, STF_M10
	INT 21H

	MOV AH, 09H
	LEA DX, STF_M11
	INT 21H

	MOV AH, 09H
	LEA DX, STF_M12
	INT 21H

	MOV AH, 01H			; USER ENTER CHOICE
	INT 21H
	MOV CL, AL
	SUB CL, 48

	CMP CL, 1			; REPORT
	JE STF_CURRENT

	CMP CL, 2			; INFO
	JE STF_NEW

	CMP CL, 3			; EXIT
	JE STAFF

	JMP STF_INVALID2	; INVALID


STF_INVALID1:
	MOV AH, 09H
	LEA DX, MSG2
	INT 21H
	JMP STAFF

STF_INVALID2:
	MOV AH, 09H
	LEA DX, MSG2
	INT 21H
	JMP STF_INFO


;---------------INVALID CHOICE
INVALID:			; INVaLID CHOICE
	MOV AH, 09H
	LEA DX, MSG2
	INT 21H
	JMP AGAIN

;--------------EXIT PROGRAM
EXIT:				; EXIT PROGRAM
	MOV AH, 09H
	LEA DX, MSG1
	INT 21H
	
	MOV AX, 4C00H
	INT 21H

	MAIN ENDP
END MAIN